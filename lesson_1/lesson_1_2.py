'''
------------------------------------------------------------------------------------------------------------------------
                           УДАЛЕНИЕ АТРИБУТА: delattr(object, name)
------------------------------------------------------------------------------------------------------------------------ 
1. del объект_класса(класс).атрибут
del cat.meal - удалить атрибут meal у объекта класса cat
------------------------------------------------------------------------------------------------------------------------ 
2. Функция delattr() удаляет атрибут у объекта по имени (строке).
🔹 Синтаксис
delattr(object, name)
object — объект
name — имя атрибута (строка)
------------------------------------------------------------------------------------------------------------------------ 
🔹 Самый базовый пример:
class Animal:
    color = 'green'
    paws = 4
cat = Animal()
dog = Animal()
setattr(cat, 'meal', 'wiskas') # - второй способ создания нового атрибута
delattr(cat, 'meal')
------------------------------------------------------------------------------------------------------------------------ 
🔹 Важные детали
✔️ Удаляет атрибут полностью
❗ Если атрибута нет → AttributeError
✔️ Работает динамически (имя как строка)
------------------------------------------------------------------------------------------------------------------------ 

'''
class Animal:
    color = 'green'
    paws = 4
cat = Animal()
dog = Animal()
setattr(cat, 'meal', 'wiskas')
print(cat.meal)
# del cat.meal # - первый способ удаления атрибута
# print(getattr(cat, 'meal', 'Атрибут не найден'))
delattr(cat, 'meal') # - второй способ удаления атрибута
print(getattr(cat, 'meal', 'Атрибут не найден'))

