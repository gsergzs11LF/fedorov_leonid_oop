'''
------------------------------------------------------------------------------------------------------------------------
                             СОЗДАНИЕ НОВОГО АТРИБУТА: setattr(object, name, value)
------------------------------------------------------------------------------------------------------------------------     
1. Класс(или объект класса).атрибут = значение атрибута
cat.meal = 'wiskas'
------------------------------------------------------------------------------------------------------------------------
2.  Функция setattr() позволяет динамически установить (или изменить) атрибут объекта.
Синтаксис:
setattr(object, name, value)

object — объект (экземпляр класса)
name — имя атрибута (строка)
value — значение атрибута
------------------------------------------------------------------------------------------------------------------------
🔹 Пример
class User:
    pass
u = User()
setattr(u, "name", "Alex")
setattr(u, "age", 25)
print(u.name)  # Alex
print(u.age)   # 25

👉 То же самое, что:
u.name = "Alex"
u.age = 25
------------------------------------------------------------------------------------------------------------------------
🔹 Когда используется
Когда имя атрибута приходит динамически (например, из input, JSON, словаря)
При работе с метапрограммированием
В тестировании (например, мок объектов)
------------------------------------------------------------------------------------------------------------------------
🔹 Важные детали
✔️ Если атрибут уже есть → он перезапишется
✔️ Если нет → он создастся
⚠️ Имя атрибута должно быть строкой: setattr(u, 123, "test")  # ошибка
⚠️ Можно создать «левые» атрибуты, которых нет в классе (будь осторожен)
------------------------------------------------------------------------------------------------------------------------
'''
# class Animal:
#     color = 'green'
#     paws = 4
# cat = Animal()
# dog = Animal()
#
# # cat.meal = 'wiskas' # - первый способ создания нового атрибута
# setattr(cat, 'meal', 'wiskas') # - второй способ создания нового атрибута
# print(cat.__dict__)

'''
------------------------------------------------------------------------------------------------------------------------
                         ПОЛУЧЕНИЕ ИМЕЮЩЕГОСЯ АТРИБУТА: getattr(object, name, default=None)
------------------------------------------------------------------------------------------------------------------------ 
1. объект_класса(или сам класс).атрибут - получение значения атрибута 
cat.meal
------------------------------------------------------------------------------------------------------------------------ 
2. Функция getattr() позволяет получить значение атрибута объекта по его имени (строке).

🔹 Синтаксис
getattr(object, name, default=None)
object — объект
name — имя атрибута (строка)
default — значение, если атрибут не найден (необязательно)
------------------------------------------------------------------------------------------------------------------------ 
🔹 Пример
class Animal:
    color = 'green'
    paws = 4
cat = Animal()
dog = Animal()
setattr(cat, 'meal', 'wiskas') # - второй способ создания нового атрибута

print(cat.meal)                     # объект_класса(или сам класс).атрибут - получение значения атрибута 
print(getattr(cat, 'meal'))         # Функция getattr() позволяет получить значение атрибута объекта по его имени (строке).

👉 То же самое:
u.name
'''
