"""
------------------------------------------------------------------------------------------------------------------------
                                    Магические метод __delattr__
------------------------------------------------------------------------------------------------------------------------
__delattr__ — это магический метод, который вызывается при удалении атрибута у объекта. 
Он перехватывает операцию del obj.attr и позволяет контролировать или изменять поведение удаления.

Когда ты пишешь:
del obj.attr

Python делает:
obj.__delattr__('attr')
------------------------------------------------------------------------------------------------------------------------
Сигнатура метода: 
def __delattr__(self, name):, 
где name — это имя атрибута, который пытаются удалить.

По умолчанию Python просто удаляет атрибут из объекта (из __dict__).
Но если ты переопределяешь __delattr__, ты берёшь этот процесс под контроль.
------------------------------------------------------------------------------------------------------------------------
Простой пример:

class User:
    def __init__(self):
        self.name = "Alex"
    def __delattr__(self, name):
        print(f"Удаляем {name}")
        object.__delattr__(self, name)
u = User()
del u.name
Здесь перед удалением выводится сообщение, а затем вызывается стандартное удаление через object.__delattr__.
------------------------------------------------------------------------------------------------------------------------
Критически важно: если внутри __delattr__ попытаться удалить атрибут через del self.name, 
будет бесконечная рекурсия, потому что это снова вызовет __delattr__. 
Поэтому всегда нужно использовать object.__delattr__(self, name).
------------------------------------------------------------------------------------------------------------------------
Ключевые моменты: 
1. __delattr__ вызывается при del obj.attr, позволяет контролировать удаление атрибутов
2. Внутри нельзя использовать del self.attr, всегда нужно вызывать object.__delattr__, иначе удаление не произойдёт.

Коротко: __delattr__ — это перехват удаления атрибутов у объекта.
------------------------------------------------------------------------------------------------------------------------
"""
from datetime import datetime

class BankAccount:
    BANK_NAME = "Легко в ИТ Банк"  # - атрибут класса
    MIN_BALANCE = 0  # - атрибут класса

    def __init__(self, owner: str, balance: int):
        self.owner = owner  # - атрибут экземпляра
        self.balance = balance # - атрибут экземпляра

    def __getattribute__(self, name): # - вызывается при любом обращении к любому атрибуту
        print(f"[LOG] "
              f"{datetime.now().strftime("%Y.%m.%d %H:%M:%S")} "
              f"Доступ к атрибуту: {name}") # - после обращения принтуем что обратились и к какому атрибуту обратились
        return object.__getattribute__(self, name) # - по сути это то же самое, что и например a1.MIN_BALANCE, только object.__getattribute__(a1, MIN_BALANCE). Просто так это выглядит изнутри

    def __setattr__(self, key, value):
        if key == 'balance' and value < self.MIN_BALANCE:
            raise ValueError (f'Баланс не может быть меньше, чем {self.MIN_BALANCE}')
        print(f"[LOG] "
              f"{datetime.now().strftime("%Y.%m.%d %H:%M:%S")} "
              f"Изменен атрибут: '{key}' на значение '{value}'")  # - после обращения принтуем что обратились и к какому атрибуту обратились
        return object.__setattr__(self, key, value)

    def __getattr__(self, name):
        print(f"[WARN] "
              f"{datetime.now().strftime("%Y.%m.%d %H:%M:%S")} "
              f"Атрибут '{name}' не найден")
        return None
    def __delattr__(self, name):
        if 'balance' == name:
            raise AttributeError (f'Нельзя удалить {name} у пользователя')
        print(f"[LOG] "
              f"{datetime.now().strftime("%Y.%m.%d %H:%M:%S")} "
              f"Удален утрибут '{name}'")
        return object.__delattr__(self, name)


    @classmethod
    def set_min_balance(cls, value: int): # - метод класса который позволяет менять минимальное значение баланса У САМОГО КЛАССА
        cls.MIN_BALANCE = value


a1 = BankAccount('Leonid', 10_000)
a2 = BankAccount('Nastya', 1_000)

del a1.balance # - вызываем наш несуществующий атрибут